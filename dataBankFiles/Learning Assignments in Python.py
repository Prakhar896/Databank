#Assignments
x = 1
x = x + 2

print(x) #(3)

x += 2 #short form of writing x = x + 2
print(x)

x -= 1
print(x)

x *= 3
print(x)

x //= 2 #Floor divsion, rounding down numbers to an int
print(x)

x %= 5 #Modulus operation, gets remainder of a division
print(x)

# Summary
# x <operator> = 2 means that 2 is applied to x with that operator
