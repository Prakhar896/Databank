# Logical operators

# not --> opposite e.g not True --> False
print(not False)
# and --> both conditions on each side must be True to give an out of True
print(True and False)
print(True and True)
print(False and False)
# or --> as long as any condition on each side is True, output is True
print(True or False)
print(False or False)

# What if we use a mix of logical operators?
print(True or False and True) # output is True
# order of precedence: not, and, or VERY IMPORTANT

